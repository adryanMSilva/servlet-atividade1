package com.example.web.utils;

public class Comentario {
	private String autor;
	private String texto;
	
	public Comentario(String autor, String texto) {
		super();
		this.autor = autor;
		this.texto = texto;
	}

	public String getAutor() {
		return autor;
	}

	public String getTexto() {
		return texto;
	}
	
	
}
